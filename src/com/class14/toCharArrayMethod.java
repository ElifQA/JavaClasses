package com.class14;

public class toCharArrayMethod {

	public static void main(String[] args) {
		/*.tocharArray 
		 * this method converts this string to a new character array.
		 */
		String str="Syntax";
		char [] array=str.toCharArray();
		for (char i:array) {
			System.out.println(i);
		}
		

	}

}
