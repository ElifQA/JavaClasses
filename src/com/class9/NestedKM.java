public class NestedKM {

	public static void main(String[] args) {


		for (int car = 0; car <= 9; car++) {
			for (int j = 0; j <= 9; j++) {
				for (int k = 0; k <= 9; k++) {
					for (int l = 0; l <= 9; l++) {
						System.out.println(car + "" + j + "" + k + "" + l);
					}
				}

			}

		}
		
		
		
	
		
		
		
		
		
		
		
		
		
		
		
		
	
		
	}

}
